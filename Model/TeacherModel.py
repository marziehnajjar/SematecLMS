class Teacher:
    def __init__(self, firstName=None, lastName=None, nationalCode=None, sex=None, birthdate=None, email=None,
                 mobile=None, address=None, registerDate=None, educationDegree=None, educationField=None,
                 insuranceNumber=None, cardNumber=None, photo=None):
        self.FirstName = firstName
        self.LastName = lastName
        self.NationalCode = nationalCode
        self.Sex = sex
        self.Birthdate = birthdate
        self.Email = email
        self.Mobile = mobile
        self.Address = address
        self.RegisterDate = registerDate
        self.EducationDegree = educationDegree
        self.EducationField = educationField
        self.InsuranceNumber = insuranceNumber
        self.CardNumber = cardNumber
        self.Photo = photo
